    private void jButtonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValiderActionPerformed
        String codeSalarie = "S" + jTextFieldCode.getText();
        String nomSalarie = jTextFieldNom.getText();
        String prenomSalarie = jTextFieldPrenom.getText();
        Date dateNaissSalarie = jDateChooserDateNaiss.getDate();
        Date dateEmbaucheSalarie = jDateChooserDateEmbauche.getDate();
        String fonctionSalarie = jTextFieldFonction.getText();
        double tauxHoraireSalarie = Double.parseDouble(jTextFieldTauxHorr.getText());
        String situationFamilialeSalarie = jTextFieldSitFamiliale.getText();
        int nbEnfantSalarie = Integer.parseInt(jTextFieldNbEnfant.getText());
        Categorie catSalarie = (Categorie) jComboBoxCat.getSelectedItem();
        Service serviceSalarie = (Service) jComboBoxService.getSelectedItem();
        
        Salarie nouveauSalarie = new Salarie(codeSalarie, nomSalarie, prenomSalarie, dateNaissSalarie, dateEmbaucheSalarie, fonctionSalarie, tauxHoraireSalarie, situationFamilialeSalarie, nbEnfantSalarie, serviceSalarie, catSalarie);
        
        try {
            //On appelle la fonction DaoSalarie.AjouterSalarie qui va permettre d'ajouter le salarie à la base de données.
            DaoSalarie.AjouterSalarie(nouveauSalarie);
            System.out.println("Les informations ont été mis à jour.");
            
        } catch (SQLException ex) {
            System.out.println("Erreur : "+ ex.getMessage());
        }
        
        this.dispose();
    }//GEN-LAST:event_jButtonValiderActionPerformed

