        // La classe IntFilter pour restreindre l'entrée aux entiers
     class IntFilter extends DocumentFilter {

         @Override
         public void insertString(FilterBypass bypass, int offset, String string, AttributeSet attr) throws BadLocationException {
             StringBuilder stringBuilder = new StringBuilder();
             stringBuilder.append(bypass.getDocument().getText(0, bypass.getDocument().getLength()));
             stringBuilder.insert(offset, string);

             if (isValid(stringBuilder.toString())) {
                 super.insertString(bypass, offset, string, attr);
             }
         }

         @Override
         public void replace(FilterBypass bypass, int offset, int length, String text, AttributeSet attrs) throws BadLocationException {
             StringBuilder stringBuilder = new StringBuilder();
            stringBuilder.append(bypass.getDocument().getText(0, bypass.getDocument().getLength()));
            stringBuilder.replace(offset, offset + length, text);

            if (isValid(stringBuilder.toString())) {
                super.replace(bypass, offset, length, text, attrs);
            }
        }

        private boolean isValid(String text) {
            if (text.isEmpty()) {
                return true;
            }
            try {
                Integer.parseInt(text);
                return true;
            } catch (NumberFormatException e) {
                return false;
            }
        }
    }

