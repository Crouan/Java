    /**
     * Méthode pour mettre à jour les champs de texte avec les informations du salarié.
     * @param salarie
     */
    public void setSalariesInfo(Salarie salarie) {
        // Remplir les champs de texte avec les informations du salarié
        jTextFieldCode.setText(salarie.getCode());
        jTextFieldNom.setText(salarie.getNom());
        jTextFieldPrenom.setText(salarie.getPrenom());
        jTextFieldDateNaissance.setText(salarie.getDateNaiss().toString());
        jTextFieldDateEmbauche.setText(salarie.getDateEmbauche().toString());
        jTextFieldFonction.setText(salarie.getFonction());
        jTextFieldTauxHoraire.setText(String.valueOf(salarie.getTauxHoraire()));
        jTextFieldSituationFamiliale.setText(salarie.getSituationFamiliale());
        jTextFieldNombreEnfants.setText(String.valueOf(salarie.getNbrEnfants()));

        // Vérifier si le service du salarié est non null avant de récupérer le code du service
        if (salarie.getService() != null) {
            jTextFieldCodeService.setText(String.valueOf(salarie.getService().getCode()));
        } else {
            jTextFieldCodeService.setText("");
        }

        // Vérifier si la catégorie du salarié est non null avant de récupérer le numéro de catégorie
        if (salarie.getCategorie() != null) {
            jTextFieldNumCathegorie.setText(salarie.getCategorie().getCode());
        } else {
            jTextFieldNumCathegorie.setText("");
        }
    }

