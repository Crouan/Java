    private void jButtonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValiderActionPerformed
        int reponse = JOptionPane.showConfirmDialog(null, "Êtes-vous sûr de vouloir modifier l'utilisateur "+ jTextFieldPrenom.getText() +" "+ jTextFieldNom.getText() +" ? ","Confirmation", JOptionPane.YES_NO_OPTION);
        if (reponse == JOptionPane.YES_OPTION) {
            // Code si Oui
            try {
                // Fermer la fenêtre actuelle
                this.dispose();

                // Supprimer le salarié en utilisant le code saisi dans le champ jTextFieldCode
                deleteSalarie(jTextFieldCode.getText());

                // Afficher la fenêtre JFrameListeSalaries
                // JFrameListeSalaries listeSalariesFrame = new JFrameListeSalaries();
                // listeSalariesFrame.setVisible(true);
                Main.main(new String[]{});
            } catch (Exception ex) {
                System.out.println("Erreur lors de La Suppression du Salarié Demandé : " + ex.getMessage());
            }
        } else {
            // Code si Non
        }
    }//GEN-LAST:event_jButtonValiderActionPerformed

